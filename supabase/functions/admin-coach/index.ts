import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { message } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');

    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    const systemPrompt = `You are MindPulse Admin Assistant, an AI coach specialized in helping managers and administrators:

1. Understand team wellbeing trends and patterns
2. Identify at-risk employees who may need support
3. Provide management coaching and leadership guidance
4. Suggest team interventions and wellness strategies
5. Help interpret moodmeter scores and survey data
6. Offer advice on creating a supportive work environment
7. Guide on handling sensitive employee wellbeing situations

Your responses should be:
- Professional yet empathetic
- Actionable with specific recommendations
- Data-driven when discussing team metrics
- Confidentiality-aware (don't share specific employee details)
- Concise (2-3 paragraphs max) but meaningful

Example responses:
- "Based on the team's moodmeter scores, I notice a pattern of stress around project deadlines. Consider implementing flexible deadlines or additional support during high-pressure periods."
- "When an employee's wellbeing score drops significantly, it's important to have a private, supportive conversation. Focus on listening and offering resources rather than making assumptions."
- "Team cohesion can be improved through regular check-ins and creating safe spaces for open communication. Weekly team surveys help identify issues early."

Remember: You're here to support managers in creating a healthy, productive work environment.`;

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: message }
        ],
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: 'Rate limit exceeded. Please try again later.' }),
          { status: 429, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: 'AI credits depleted. Please add credits to continue.' }),
          { status: 402, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        );
      }
      throw new Error(`AI gateway error: ${response.status}`);
    }

    const data = await response.json();
    const aiResponse = data.choices[0].message.content;

    return new Response(
      JSON.stringify({ response: aiResponse }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  } catch (error) {
    console.error('Error in admin-coach:', error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : 'Unknown error' }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});

