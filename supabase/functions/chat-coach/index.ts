import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { message } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');

    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    const systemPrompt = `You are MindPulse, an empathetic AI mental health coach for employees. Your role is to:

1. Provide supportive, actionable advice on wellbeing, stress, motivation, and work-life balance
2. Help employees set and achieve goals
3. Offer coaching on interpersonal interactions and team dynamics
4. Suggest practical strategies for managing workload and energy
5. Be warm, understanding, and non-judgmental
6. Keep responses concise (2-3 paragraphs max) but meaningful
7. Ask thoughtful follow-up questions to understand the employee better

Example coaching responses:
- "It sounds like you're feeling overwhelmed with your workload. Have you considered breaking your tasks into smaller, manageable steps? Sometimes just completing one small task can give you momentum."
- "I notice you mentioned tension in team meetings. One approach could be to ask a colleague for their perspective privately. Opening up dialogue can often ease tension. How do you feel about trying that?"
- "Taking small breaks is crucial for maintaining focus and energy. Even a 5-minute walk can reset your mind. When was the last time you took a proper break?"

Remember: You're here to support, guide, and empower employees to thrive.`;

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: message }
        ],
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: 'Rate limit exceeded. Please try again later.' }),
          { status: 429, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: 'AI credits depleted. Please add credits to continue.' }),
          { status: 402, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        );
      }
      throw new Error(`AI gateway error: ${response.status}`);
    }

    const data = await response.json();
    const aiResponse = data.choices[0].message.content;

    return new Response(
      JSON.stringify({ response: aiResponse }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  } catch (error) {
    console.error('Error in chat-coach:', error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : 'Unknown error' }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
